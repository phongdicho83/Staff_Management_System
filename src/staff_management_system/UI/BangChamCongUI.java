    public void readData() {
        BangChamCongBUS bus = new BangChamCongBUS();
        try {
            listChamCong = bus.getBangChamCongs();
        } catch (Exception ex) {
            Logger.getLogger(BangChamCongUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void loadData(ArrayList<BangChamCong> list) {
        Vector header = new Vector();
        header.add("Mã chấm công");
        header.add("Mã nhân viên");
        header.add("Ngày");
        header.add("Trạng thái");
        model = new DefaultTableModel(header, 0);

        String[] data = new String[header.size()];

        for (int i = 0; i < list.size(); i++) {
            data[0] = list.get(i).getMaChamCong();
            data[1] = list.get(i).getMaNV();
            data[2] = formatter.format(list.get(i).getDate());
            data[3] = list.get(i).getTrangThai();
            model.addRow(data);
        }

        tblChamCong.setModel(model);
    }


