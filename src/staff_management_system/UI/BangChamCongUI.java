    private void btLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLuuActionPerformed
        try {
            BangChamCong chamCong = new BangChamCong();
            chamCong.setMaChamCong(txMaChamCong.getText());
            chamCong.setMaNV(txMaNV.getText());
            chamCong.setDate(dateChooser.getDate());
            chamCong.setTrangThai(String.valueOf(cbTrangThai.getSelectedItem()));

            BangChamCongBUS bus = new BangChamCongBUS();
            bus.addChamCong(chamCong);
            readData();
            loadData(listChamCong);
            btLammoiActionPerformed(evt);

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(jPanel1, e.getMessage(), "Lỗi");
        }

    }//GEN-LAST:event_btLuuActionPerformed

    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        BangChamCong chamCong = new BangChamCong();
        chamCong.setMaChamCong(txMaChamCong.getText());
        chamCong.setMaNV(txMaNV.getText());
        chamCong.setDate(dateChooser.getDate());
        chamCong.setTrangThai(String.valueOf(cbTrangThai.getSelectedItem()));

        int i = MessageDialogHelper.showConFirmDialog(jPanel1, "Đồng ý cập nhật?", "Xác nhận");
        if (i == JOptionPane.NO_OPTION) {
            return;
        }

        BangChamCongBUS bus = new BangChamCongBUS();
        if (bus.updateChamCong(chamCong)) {
            MessageDialogHelper.showMessageDialog(jPanel1, "Cập nhật thông tin chấm công thành công!", "Thông báo");
            readData();
            loadData(listChamCong);
            btLammoiActionPerformed(evt);
        } else
            MessageDialogHelper.showErrorDialog(jPanel1, "Cập thông thông tin chấm công thất bại", "Lỗi");
    }//GEN-LAST:event_btCapNhatActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        int i = tblChamCong.getSelectedRow();

        int opt = MessageDialogHelper.showConFirmDialog(jPanel1, "Đồng ý xóa thông tin chấm công?", "Xác nhận");
        if (opt == JOptionPane.NO_OPTION) {
            return;
        }
        
        BangChamCongBUS bus = new BangChamCongBUS();
        if (bus.delChamCong(listChamCong.get(i).getMaChamCong())) {
            listChamCong.remove(i);
            MessageDialogHelper.showMessageDialog(jPanel1, "Xóa thông tin chấm công thành công!", "Thông báo");
            readData();
            loadData(listChamCong);
            btLammoiActionPerformed(evt);
        } else {
            MessageDialogHelper.showErrorDialog(jPanel1, "Xóa thông tin chấm công không thành công", "Lỗi");
        }

    }//GEN-LAST:event_btDeleteActionPerformed

