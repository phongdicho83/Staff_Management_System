    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        // TODO add your handling code here:
        int i = tblPhongBan.getSelectedRow();
        int opt = MessageDialogHelper.showConFirmDialog(jLabel1, "Đồng ý xóa thông tin phòng ban?", "Xác nhận");
        if (opt == JOptionPane.NO_OPTION) {
            return;
        }
        
        PhongBanBUS bus = new PhongBanBUS();
        if (bus.delPhongBan(listPhongBan.get(i).getMaPhongBan())) {
            listPhongBan.remove(i);
            MessageDialogHelper.showMessageDialog(jLabel1, "Xóa thông tin phòng ban thành công!", "Thông báo");
            readData();
            loadData(listPhongBan);
            btLamMoiActionPerformed(evt);
        } else {
            MessageDialogHelper.showErrorDialog(jLabel1, "Xóa thông tin phòng ban không thành công", "Lỗi");
        }
    }//GEN-LAST:event_btDeleteActionPerformed

