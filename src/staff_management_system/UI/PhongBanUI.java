    public void readData() {
        PhongBanBUS bus = new PhongBanBUS();
        try {
            listPhongBan = bus.getPhongBans();
        } catch (Exception ex) {
            Logger.getLogger(PhongBanUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void loadData(ArrayList<PhongBan> list) {
        Vector header = new Vector();
        header.add("Mã phòng ban");
        header.add("Tên phòng ban");
        header.add("Mã công việc");
        header.add("Địa chỉ");
        model = new DefaultTableModel(header, 0);

        String[] data = new String[header.size()];

        for (int i = 0; i < list.size(); i++) {
            data[0] = list.get(i).getMaPhongBan();
            data[1] = list.get(i).getTenPhongBan();
            data[2] = list.get(i).getMaCongViec();
            data[3] = list.get(i).getDiaChi();
            model.addRow(data);
        }

        tblPhongBan.setModel(model);
    }
