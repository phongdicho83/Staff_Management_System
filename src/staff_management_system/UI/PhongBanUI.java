    private void btLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLuuActionPerformed
        // TODO add your handling code here:
            PhongBan phongBan = new PhongBan();
            phongBan.setMaPhongBan(txtMaPhongBan.getText());
            phongBan.setTenPhongBan(txtTenPhongBan.getText());
            phongBan.setMaCongViec(txtMaCongViec.getText());
            phongBan.setDiaChi(txtDiaChi.getText());
            
        int i = MessageDialogHelper.showConFirmDialog(jLabel1, "Đồng ý cập nhật?", "Xác nhận");
        if (i == JOptionPane.NO_OPTION) {
            return;
        }

        PhongBanBUS bus = new PhongBanBUS();
        if (bus.updatePhongBan(phongBan)) {
            MessageDialogHelper.showMessageDialog(jLabel1, "Cập nhật thông tin phòng ban thành công!", "Thông báo");
            readData();
            loadData(listPhongBan);
            btLamMoiActionPerformed(evt);
        } else
            MessageDialogHelper.showErrorDialog(jLabel1, "Cập thông thông tin thất bại", "Lỗi");
    }//GEN-LAST:event_btLuuActionPerformed

