                temp = jTextField1.getText();
                kq=bus.timKiem1(4,temp);
                if(kq.isEmpty())
                    JOptionPane.showMessageDialog(null, "Không có người thân nào mang giới tính như vậy");
                ganDS(kq);
                break;

