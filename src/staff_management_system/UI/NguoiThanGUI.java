    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        if (i >= 0) {
            NguoiThan nt;
            nt = dsnt.get(i);
            cbMaNV.setSelectedItem(nt.getMaNV());
            txSTT.setText(nt.getStt());
            txTen.setText(nt.getHoTen());
            cbMQH.setSelectedItem(nt.getMoiQuanHe());
            if (nt.getGioiTinh().equalsIgnoreCase("nam")) {
                jRadioButton1.setSelected(true);
            } else if (nt.getGioiTinh().equalsIgnoreCase("nữ")) {
                jRadioButton2.setSelected(true);
            } else if (nt.getGioiTinh().equalsIgnoreCase("khác")) {
                jRadioButton3.setSelected(true);
            }
            txNam.setText(nt.getNamSinh());
        }
        cbMaNV.setEnabled(false);
    }//GEN-LAST:event_jTable1MouseClicked

    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        if(cbMaNV.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Chưa chọn mã nhân viên!");
            cbMaNV.requestFocus(true);
            return;
        }
        if ("".equals(txTen.getText())){
            JOptionPane.showMessageDialog(null, "Chưa điền tên người thân!");
            txTen.requestFocus(true);
            return;
        }
        if("".equals(txNam.getText())){
            JOptionPane.showMessageDialog(null, "Chưa điền năm sinh của người thân!");
            txNam.requestFocus(true);
            return;
        }

        NguoiThan nt = new NguoiThan();

        if (!bus.checkPK(cbMaNV.getSelectedItem() + "", txSTT.getText())) {
            JOptionPane.showMessageDialog(null, "Mã số này đã tồn tại");
            txSTT.selectAll();
            txSTT.requestFocus(true);
            return;
        }

        nt.setMaNV(cbMaNV.getSelectedItem() + "");
        nt.setStt(txSTT.getText());
        nt.setHoTen(txTen.getText());
        nt.setMoiQuanHe(cbMQH.getSelectedItem() + "");
        if (jRadioButton1.isSelected()) {
            nt.setGioiTinh("Nam");
        } else if (jRadioButton2.isSelected()) {
            nt.setGioiTinh("Nữ");
        } else if (jRadioButton3.isSelected()) {
            nt.setGioiTinh("Khác");
        }
        nt.setNamSinh(txNam.getText());
        bus.themNT(nt);

        Vector row = new Vector();
        row.add(nt.getMaNV());
        row.add(nt.getStt());
        row.add(nt.getHoTen());
        row.add(nt.getMoiQuanHe());
        row.add(nt.getGioiTinh());
        row.add(nt.getNamSinh());
        model.addRow(row);
        jTable1.setModel(model);
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Xác nhận sửa ", "", JOptionPane.YES_NO_OPTION);
        if (i == 1) {
            return;
        }
        i = jTable1.getSelectedRow();

        if (i >= 0) {
            NguoiThan nt = new NguoiThan();
            nt.setMaNV(cbMaNV.getSelectedItem() + "");
            nt.setStt(txSTT.getText());
            nt.setHoTen(txTen.getText());
            nt.setMoiQuanHe(cbMQH.getSelectedItem() + "");
            if (jRadioButton1.isSelected()) {
                nt.setGioiTinh("Nam");
            } else if (jRadioButton2.isSelected()) {
                nt.setGioiTinh("Nữ");
            } else if (jRadioButton3.isSelected()) {
                nt.setGioiTinh("Khác");
            }
            nt.setNamSinh(txNam.getText());
            bus.suaNT(nt);

            model.setValueAt(nt.getMaNV(), i, 0);
            model.setValueAt(nt.getStt(), i, 1);
            model.setValueAt(nt.getHoTen(), i, 2);
            model.setValueAt(nt.getMoiQuanHe(), i, 3);
            model.setValueAt(nt.getGioiTinh(), i, 4);
            model.setValueAt(nt.getNamSinh(), i, 5);
            jTable1.setModel(model);
        }
    }//GEN-LAST:event_btSuaActionPerformed

    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Xác nhận xóa ", "", JOptionPane.YES_NO_OPTION);
        if (i == 1) {
            return;
        }
        i = jTable1.getSelectedRow();
        if (i >= 0) {
            bus.xoaNT(i);
            model.removeRow(i);
            ganDS(dsnt);
            jTable1.setModel(model);
        }
    }//GEN-LAST:event_btXoaActionPerformed

