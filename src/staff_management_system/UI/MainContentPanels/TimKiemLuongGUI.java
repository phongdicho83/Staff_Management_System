    private void ganDS(ArrayList<TimKiemLuong> ds) {
        Vector header = new Vector();
        header.add("Mã nhân viên");
        header.add("Tên");
        header.add("Tháng năm");
        header.add("Tổng giờ tăng ca");
        header.add("Số tiền được hưởng");
        header.add("Số ngày vắng có phép");
        header.add("Số ngày vắng không phép");
        header.add("Số ngày nghỉ ốm");
        header.add("Lương bị trừ");
        header.add("Lương hiện hành");
        model = new DefaultTableModel(header, 0);
        Vector row;
        for (staff_management_system.DTO.TimKiemLuong tkl : ds) {
            row = new Vector();
            row.add(tkl.getMaNV());
            row.add(tkl.getTen());
            row.add(tkl.getThangNam());
            row.add(tkl.getTongGioTangCa());
            row.add(tkl.getSoTienDuocHuong());
            row.add(tkl.getSoNgayVangCoPhep());
            row.add(tkl.getSoNgayVangKhongPhep());
            row.add(tkl.getSoNgayNghiOm());
            row.add(tkl.getLuongBiTru());
            row.add(tkl.getLuongHienHanh());
            model.addRow(row);
        }
        jTable1.setModel(model);
    }
    
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField1.getText().equals("")||jComboBox1.getSelectedIndex()==0||jComboBox2.getSelectedIndex()==0){
                JOptionPane.showMessageDialog(null, "chưa điền đủ thông tin");
            ganDS(dstkl);
            trangthai = 0;
            return;
        }
        ArrayList<TimKiemLuong> ds = new ArrayList();
        String s = jComboBox1.getSelectedItem()+"-"+jComboBox2.getSelectedItem();
        for(TimKiemLuong tk:dstkl)
            if(tk.getMaNV().contains(jTextField1.getText().toUpperCase())&&tk.getThangNam().equals(s))
        ds.add(tk);
        ganDS(ds);
        dstemp=ds;
        trangthai = 1;
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        if (i >= 0 && trangthai==0) {
            tkl = dstkl.get(i);
        }
        else if(i>=0)
            tkl = dstemp.get(i);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JOptionPane.showMessageDialog(null, "Đã lấy thông tin");
        try {
            home.updateThongTin(tkl);
        } catch (Exception ex) {
            Logger.getLogger(TimKiemLuongGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        trangthai =0;
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

