    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        int i = tblChamCong.getSelectedRow();

        int opt = MessageDialogHelper.showConFirmDialog(jScrollPane1, "Đồng ý xóa thông tin chấm công?", "Xác nhận");
        if (opt == JOptionPane.NO_OPTION) {
            return;
        }

        BangChamCongBUS bus = new BangChamCongBUS();
        if (bus.delChamCong(dscc.get(i))) {
            dscc.remove(i);
            MessageDialogHelper.showMessageDialog(jScrollPane1, "Xóa thông tin chấm công thành công!", "Thông báo");
            readData();
            loadData(dscc);
            btLammoiActionPerformed(evt);
        } else {
            MessageDialogHelper.showErrorDialog(jScrollPane1, "Xóa thông tin chấm công không thành công", "Lỗi");
        }
    }//GEN-LAST:event_btDeleteActionPerformed

    private void btCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapNhatActionPerformed
        BangChamCong chamCong = new BangChamCong();
        chamCong.setMaNV(txMaNV.getText());
        chamCong.setDate(dateChooser.getDate());
        chamCong.setTrangThai(String.valueOf(cbTrangThai.getSelectedItem()));

        int i = MessageDialogHelper.showConFirmDialog(jScrollPane1, "Đồng ý cập nhật?", "Xác nhận");
        if (i == JOptionPane.NO_OPTION) {
            return;
        }

        BangChamCongBUS bus = new BangChamCongBUS();
        if (bus.updateChamCong(chamCong)) {
            MessageDialogHelper.showMessageDialog(jScrollPane1, "Cập nhật thông tin chấm công thành công!", "Thông báo");
            readData();
            loadData(dscc);
            btLammoiActionPerformed(evt);
        } else
        MessageDialogHelper.showErrorDialog(jScrollPane1, "Cập thông thông tin chấm công thất bại", "Lỗi");
    }//GEN-LAST:event_btCapNhatActionPerformed

