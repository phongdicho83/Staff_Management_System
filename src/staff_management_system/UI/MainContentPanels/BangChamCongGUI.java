    public void loadData(ArrayList<BangChamCong> list) {
        Vector header = new Vector();
        header.add("Mã nhân viên");
        header.add("Ngày");
        header.add("Trạng thái");
        model = new DefaultTableModel(header, 0);

        String[] data = new String[header.size()];

        for (int i = 0; i < list.size(); i++) {
            data[0] = list.get(i).getMaNV();
            data[1] = formatter.format(list.get(i).getDate());
            data[2] = list.get(i).getTrangThai();
            model.addRow(data);
        }

        tblChamCong.setModel(model);
    }

