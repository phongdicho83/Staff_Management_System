    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        NhanVien nhanvien = new NhanVien();
        if ("".equals(txtMaNhanVien.getText())){
            JOptionPane.showMessageDialog(null, "Chưa điền mã nhân viên");
            txtMaNhanVien.requestFocus(true);
            return;
        }
        if ("".equals(txtHo.getText())){
            JOptionPane.showMessageDialog(null, "Chưa điền họ nhân viên");
            txtHo.requestFocus(true);
            return;
        }
        if ("".equals(txtTen.getText())){
            JOptionPane.showMessageDialog(null, "Chưa điền tên nhân viên");
            txtTen.requestFocus(true);
            return;
        }
        if (null==jDateChooserNgaySinh.getDate()){
            JOptionPane.showMessageDialog(null, "Chưa điền ngày sinh");
            jDateChooserNgaySinh.requestFocus(true);
            return;
        }
        if ("".equals(txtSoDienThoai.getText())){
            JOptionPane.showMessageDialog(null, "Chưa điền số điện thoại");
            txtSoDienThoai.requestFocus(true);
            return;
        }
        if ("".equals(txtMaPhongBan.getText())){
            JOptionPane.showMessageDialog(null, "Chưa điền mã phòng ban");
            txtMaPhongBan.requestFocus(true);
            return;
        }
        if (!bus.checkPK(txtMaNhanVien.getText(),txtMaPhongBan.getText())) {
            JOptionPane.showMessageDialog(null, "Mã số này đã tồn tại");
            return;
        }
         try {
            NhanVien nhanVien = new NhanVien();
            nhanVien.setMaNV(txtMaNhanVien.getText());
            nhanVien.setHo(txtHo.getText());
            nhanVien.setTen(txtTen.getText());
            nhanVien.setNgaySinh(jDateChooserNgaySinh.getDate());
            nhanVien.setSoDienThoai(txtSoDienThoai.getText());
            if (rbNam.isSelected()) {
            nhanVien.setGioiTinh("Nam");
            } else if (rbnu.isSelected()) {
            nhanVien.setGioiTinh("Nữ");
            } else if (rbkhac.isSelected()) {
            nhanVien.setGioiTinh("Khác");
        }
            nhanVien.setMaPhongBan(txtMaPhongBan.getText());
            nhanVien.setNgayVaoLam(jDateChooserNgayvaolam.getDate());

            NhanVienBUS bus = new NhanVienBUS();
            bus.addNhanVien(nhanVien);
            readData();
            loadData(dsnv);
            btmoiActionPerformed(evt);

        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(jScrollPane1, e.getMessage(), "Lỗi");
             
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btcapnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcapnhatActionPerformed
        // TODO add your handling code here:
        int opt = JOptionPane.showConfirmDialog(null, "Xác nhận sửa ", "", JOptionPane.YES_NO_OPTION);
        if (opt == 1) {
            return;
        }
        int i = tblnhanvien.getSelectedRow();

        if (i >= 0) {
            if (!bus.checkPK(txtMaNhanVien.getText(),txtMaPhongBan.getText())) {
            JOptionPane.showMessageDialog(null, "Mã số này đã tồn tại");
            return;
        }
         NhanVien nhanVien = new NhanVien();
            nhanVien.setMaNV(txtMaNhanVien.getText());
            nhanVien.setHo(txtHo.getText());
            nhanVien.setTen(txtTen.getText());
            nhanVien.setNgaySinh(jDateChooserNgaySinh.getDate());
            nhanVien.setSoDienThoai(txtSoDienThoai.getText());
            if (rbNam.isSelected()) {
                nhanVien.setGioiTinh("Nam");
            } else if (rbnu.isSelected()) {
                nhanVien.setGioiTinh("Nữ");
            } else if (rbkhac.isSelected()) {
                nhanVien.setGioiTinh("Khác");
            }
               
            nhanVien.setMaPhongBan(txtMaPhongBan.getText());
            nhanVien.setNgayVaoLam(jDateChooserNgayvaolam.getDate());

        int ii = MessageDialogHelper.showConFirmDialog(jScrollPane1, "Đồng ý cập nhật?", "Xác nhận");
        if (ii == JOptionPane.NO_OPTION) {
            return;
        }
   

        NhanVienBUS bus = new NhanVienBUS();                
        if (bus.updateNhanVien(nhanVien)) {
            MessageDialogHelper.showMessageDialog(jScrollPane1, "Cập nhật thông tin nhân viên thành công!", "Thông báo");
            readData();
            loadData(dsnv);
            btmoiActionPerformed(evt);
        } else
            MessageDialogHelper.showErrorDialog(jScrollPane1, "Cập thông thông tin nhân viên thất bại", "Lỗi"); 
        }
    }//GEN-LAST:event_btcapnhatActionPerformed

