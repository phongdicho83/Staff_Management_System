    public void loadData(ArrayList<NhanVien> list) {
        Vector header = new Vector();
        header.add("Mã nhân viên");
        header.add("Họ");
        header.add("Tên");
        header.add("Ngày sinh");
        header.add("Số điện thoại");
        header.add("Giới tính");
        header.add("Phòng ban");
        header.add("Ngày kí hợp đồng");
        model = new DefaultTableModel(header, 0);

        String[] data = new String[header.size()];

        for (int i = 0; i < list.size(); i++) {
            data[0] = list.get(i).getMaNV();
            data[1] = list.get(i).getHo();
            data[2] = list.get(i).getTen();
            data[3] = formatter.format(list.get(i).getNgaySinh());
            data[4] = list.get(i).getSoDienThoai();
            data[5] = list.get(i).getGioiTinh();
            data[6] = list.get(i).getMaPhongBan();
            data[7] = formatter.format(list.get(i).getNgayVaoLam());
            model.addRow(data);
        }

        tblnhanvien.setModel(model);
    }

