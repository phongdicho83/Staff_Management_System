    public void loadData(ArrayList<TienThuongDTO> list) {
        Vector header = new Vector();
        header.add("Mã tiền thưởng");
        header.add("Tên tiền thưởng");
        header.add("Tháng");
        header.add("Số tiền");
        model = new DefaultTableModel(header, 0);

        String[] data = new String[header.size()];

        for (int i = 0; i < list.size(); i++) {
            data[0] = list.get(i).getMaTienThuong();
            data[1] = list.get(i).getTenTienThuong();
            data[2] = String.valueOf(list.get(i).getThang());
            data[3] = String.valueOf(list.get(i).getTienThuong());
            model.addRow(data);
        }

        tbTienThuong.setModel(model);
    }

