    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int i = tbTienThuong.getSelectedRow();

        int opt = MessageDialogHelper.showConFirmDialog(jPanel1, "Đồng ý xóa thông tin tiền thưởng?", "Xác nhận");
        if (opt == JOptionPane.NO_OPTION) {
            return;
        }

        TienThuongBUS bus = new TienThuongBUS();
        if (bus.delTienThuong(dstt.get(i).getMaTienThuong())) {
            dstt.remove(i);
            MessageDialogHelper.showMessageDialog(jPanel1, "Xóa thông tin tiền thưởng thành công!", "Thông báo");
            readData();
            loadData(dstt);
            btnLamMoiActionPerformed(evt);
        } else {
            MessageDialogHelper.showErrorDialog(jPanel1, "Xóa thông tin thưởng không thành công", "Lỗi");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        TienThuongDTO tienthuong = new TienThuongDTO();
        tienthuong.setMaTienThuong(txtMaTienThuong.getText());
        tienthuong.setTenTienThuong(txtTenTienThuong.getText());
         tienthuong.setThang(Integer.parseInt(spinThang.getValue()+""));
         tienthuong.setTienThuong(Long.valueOf(txtSoTien.getText()));

        int i = MessageDialogHelper.showConFirmDialog(jPanel1, "Đồng ý cập nhật?", "Xác nhận");
        if (i == JOptionPane.NO_OPTION) {
            return;
        }

        TienThuongBUS bus = new TienThuongBUS();
        if (bus.updateTienThuong(tienthuong)) {
            MessageDialogHelper.showMessageDialog(jPanel1, "Cập nhật thông tin tiền thưởng thành công!", "Thông báo");
            readData();
            loadData(dstt);
            btnLamMoiActionPerformed(evt);
        } else
            MessageDialogHelper.showErrorDialog(jPanel1, "Cập thông thông tin tiền thưởng thất bại", "Lỗi");
    }//GEN-LAST:event_btnSuaActionPerformed

